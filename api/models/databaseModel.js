const mongoose = require('mongoose');

const databaseSchema = mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  projectId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'Project',
    required: true
  },
  createdAt: {
    type: Date,
    default: Date.now
  }
});

module.exports = mongoose.model('Database', databaseSchema);
